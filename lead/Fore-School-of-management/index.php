       <p><span style="color:#da3301d9;font-size:15px;font-weight:bold">PLACEMENTS@FORE</span><br>

Placements@FORE is perhaps one of the most important activity from the students perspective. The Institute engages the 
Industry by inviting them to the campus for guest lectures, seminars, MDP’s, live projects, mentoring sessions etc.
