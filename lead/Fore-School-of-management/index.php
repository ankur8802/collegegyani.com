Summer Internship Program (SIP) -Summer Internship Program at FORE is a great mechanism for the students to
 engage and evaluate interesting industry segments and engineering their resources to make a pitch for a pre-placement
 offer (PPO). Summer Internship is a part of the course curriculum for the students. The Internship usually starts in 
 the 2nd week of April and ends in the 2nd week of June with duration of 8 weeks. After the completion of the Summer Internship 
 the students are required to submit the Internship report along with the company feedback.
 Thereafter, the students are evaluated based on the presentations of their Summer Internship projects..</p>
