                    <p>Fortune Institute of International Business Admission 2020: PGDM, Eligibility, Cutoff, Fee Structure
Fortune Institute of International Business is a premier Business school situated in Delhi. At present, FIIB, New Delhi offers a two-year full-time Post-Graduate Diploma in Management (PGDM) course under several disciplines. 

The basic eligibility for PGDM admission is a Bachelor’s degree or equivalent after 10+2 or equivalent.
Candidates will be shortlisted as per CAT/MAT/ATMA/XAT/CMAT scores.
Shortlisted candidates will be further assessed on the basis of candidate’s performance in the Essay Writing, Group Discussion (GD) and Personal Interview (PI) rounds, conducted by FIIB.
Application form for admission to FIIB, New Delhi can be filled in both online as well as offline mode. 
The application fee of INR 1500 can be submitted via Credit/ Debit card, Netbanking or by a Demand Draft in favor of Fortune Institute of Business, New Delhi.</p>
