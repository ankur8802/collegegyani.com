Recognised as the ‘Voice of Business in Goa’, Business Goa has been exploring and recording pivotal moments in business in Goa- and documenting entrepreneurial success in all its forms.

As Goa’s only business magazine, we have been at the forefront of highlighting the economic and industrial issues that relate to our state.

Our reportage of events and business milestones in the state has been of high standard. Our vision and mission remains focused – to connect to every stakeholder of Goa’s business sector and be the pioneering platform for spotting business trends. A major significant value addition to the brand is the annual Business Goa Awards for Corporate Excellence that we have been successfully hosting since 2012. It is, in fact, Goa’s most prized and awaited corporate event and is attended by the industry captains, entrepreneurs and corporate stalwarts.

From personal finance to runway fashion, Business Goa continually strives to celebrate those movers and shakers who have defined and redefined Goan business with their creativity and tenacity. </p>
